const requirementAr = {};

const recommendationAr = {};

const assetAr = {};

const policyAr = {};
const valueAr = {};

export function trStatus(status, language) {
  return statusLabels[language]?.[status] || status;
}

export function trDomain(domain, language) {
  if (typeof domain === "string") {
    return language === "ar" ? domainNameAr[domain] || domain : domain;
  }

  return language === "ar" ? domain.nameAr || domainNameAr[domain.name] || domain.name : domain.name;
}

export function trRequirement(requirement, language) {
  if (language !== "ar") return requirement;
  return { ...requirement, ...(requirementAr[requirement.id] || {}) };
}

export function trRecommendation(item, language) {
  if (language !== "ar") return { ...item, domain: trDomain(item.domain, language) };
  const translated = recommendationAr[item.title] || {};
  return {
    ...item,
    ...translated,
    title: item.titleAr || translated.title || item.title,
    description: item.descriptionAr || translated.description || item.description,
    analysis: item.analysisAr || translated.analysis || item.analysis,
    action: item.actionAr || translated.action || item.action,
    domain: item.domainAr || trDomain(item.domain, language),
  };
}

export function trAsset(asset, language) {
  if (language !== "ar") return asset;
  return {
    ...asset,
    ...(assetAr[asset.id] || {}),
    domain: trDomain(asset.domain, language),
  };
}

export function trPolicy(policy, language) {
  if (language !== "ar") return { ...policy, domain: trDomain(policy.domain, language) };
  return {
    ...policy,
    ...(policyAr[policy.id] || {}),
    domain: trDomain(policy.domain, language),
  };
}

export function trValue(value, language) {
  return language === "ar" ? valueAr[value] || trStatus(value, language) : value;
}
